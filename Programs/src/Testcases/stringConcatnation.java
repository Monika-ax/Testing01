package Testcases;

public class stringConcatnation {
	public static void main(String[]arg) {
		int a = 100;
		int b = 200;
		System.out.println(a+b);
		String x = "Hello";
		String y = "World";
		System.out.println(x+y);
		System.out.println(x+y+a+b);
		System.out.println(x+y+(a+b));
		
				
	}

}
